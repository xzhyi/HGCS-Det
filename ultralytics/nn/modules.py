class SRM(nn.Module):
    def __init__(self, c1):
        super().__init__()
        self.cfc = nn.Conv1d(c1, c1, kernel_size=2, groups=c1,
                             bias=False)
        self.bn = nn.BatchNorm1d(c1)

    def forward(self, x):
        b, c, h, w = x.shape
        # style pooling
        mean = x.reshape(b, c, -1).mean(-1).unsqueeze(-1)
        std = x.reshape(b, c, -1).std(-1).unsqueeze(-1)
        u = torch.cat([mean, std], dim=-1)
        # style integration
        z = self.cfc(u)
        z = self.bn(z)
        g = torch.sigmoid(z)
        g = g.reshape(b, c, 1, 1)
        return x * g.expand_as(x)


